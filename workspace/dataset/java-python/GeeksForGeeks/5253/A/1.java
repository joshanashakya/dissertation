

// Java program to perform 
// sum of cubes of first  
// n even natural numbers 
  
public class GFG 
{ 
    public static int cubesum(int n) 
    { 
                  
        return 2 * n * n * (n + 1) * (n + 1); 
    } 
      
  
    // Driver function 
    public static void main(String args[]) 
    { 
        int a = 8; 
        System.out.println(cubesum(a)); 
          
    } 
} 
  
