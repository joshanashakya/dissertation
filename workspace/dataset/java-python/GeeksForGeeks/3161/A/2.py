

# Python3 program to find  
# area of an Ellipse. 
  
# Function to find area 
# of an ellipse. 
def findArea(a, b): 
      
    # formula to find the  
    # area of an Ellipse. 
    Area = 3.142 * a * b ; 
      
    # Display the result 
    print("Area:", round(Area, 2)); 
  
# Driver code 
a = 5;  
b = 4; 
  
findArea(a, b); 
  
