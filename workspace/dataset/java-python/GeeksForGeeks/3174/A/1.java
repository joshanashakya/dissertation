class GFG  
{  
      
    // Function to return the number of  
    // distinct ways to represent n  
    // as the sum of two integers  
    static int ways(int n)  
    {  
        return n / 2;  
    }  
      
    // Driver code  
    public static void main(String args[])  
    {  
        int n = 2;  
      
        System.out.println(ways(n));  
    }  
} 
  
