

// Java Program to find 
// the last digit of X in base Y 
class GFG  
{ 
  
// Function to find the last 
// digit of X in base Y 
static void last_digit(int X, int Y) 
{ 
    System.out.print(X % Y); 
} 
  
// Driver code 
public static void main(String []args) 
{ 
    int X = 55, Y = 3; 
    last_digit(X, Y); 
} 
} 
  
