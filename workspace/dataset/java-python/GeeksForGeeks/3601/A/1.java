

// Java program to find number of pieces 
// of circle after N cuts 
import java.util.*; 
  
class solution 
{ 
  
// Function to find number of pieces 
// of circle after N cuts 
static int countPieces(int N) 
{ 
    return 2 * N; 
} 
  
// Driver program 
public static void main(String args[]) 
{ 
    int N = 100; 
  
    System.out.println(countPieces(N)); 
  
} 
  
} 

