

# Program to find weighted mean of first 
# n natural numbers using formula. 
  
# Returns weighted mean assuming for numbers 
# 1, 2, ..n and weights 1, 2, .. n 
def weightedMean(n): 
  
    return (2 * n + 1) / 3
  
# Driver program to test the function. 
n = 10
print(int(weightedMean(n))) 
