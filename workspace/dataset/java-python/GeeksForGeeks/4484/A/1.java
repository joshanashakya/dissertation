

// Java program to find 
// the area of a square 
  
class GFG 
{ 
  
// function to find the area 
static int area_fun(int side) 
{ 
int area = side * side;  
return area; 
} 
  
// Driver code 
public static void main(String arg[])  
{ 
    int side = 4; 
    int area = area_fun(side); 
    System.out.println(area); 
} 
} 
  
