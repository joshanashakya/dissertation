

// Java Program to multiply 
// a number with 3.5 
  
class GFG { 
          
    static int multiplyWith3Point5(int x) 
    { 
        return (x<<1) + x + (x>>1); 
    }  
      
    /* Driver program to test above functions*/
    public static void main(String[] args) 
    { 
        int x = 2;  
        System.out.println(multiplyWith3Point5(x)); 
    } 
} 
  
