

// JAVA program to find the nth EVEN number 
  
class GFG 
{ 
    // Function to find the nth odd number 
    static int nthEven(int n) 
    { 
        return (2 * n); 
    } 
      
    // Driver code 
    public static void main(String [] args) 
    { 
        int n = 10; 
        System.out.println(nthEven(n)); 
          
    } 
} 
  
