

# Python3 implementation to find  
# sum of first n even numbers 
      
# function to find sum of 
# first n even numbers 
def evensum(n): 
    return n * (n + 1) 
  
# Driver Code 
n = 20
print("sum of first", n, "even number is: ", 
       evensum(n)) 
  
