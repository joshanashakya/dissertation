

// Java program to find 
// Circumference of a square 
  
import java.io.*; 
class GFG  
{ 
    int Circumference(int a) 
    { 
        return 4 * a; 
    } 
  
    // Driver code 
    public static void main(String args[]) 
    { 
        GFG obj = new GFG(); 
        int a = 5; 
        System.out.println("Circumference of " +  
                                "a square is " +  
                          obj.Circumference(a)); 
    } 
} 
  
