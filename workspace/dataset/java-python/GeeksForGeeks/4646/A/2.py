

# Python3 program to convert  
# centimeter into meter 
# and kilometer 
cm = 1000; 
  
# Converting centimeter  
# into meter and kilometer 
meter = cm / 100.0; 
kilometer = cm / 100000.0; 
  
# Driver Code 
print("Length in meter = " , 
               meter , "m"); 
print("Length in Kilometer = ",  
             kilometer , "km"); 
  
