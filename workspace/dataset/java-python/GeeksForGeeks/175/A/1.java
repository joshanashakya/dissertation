

// Java program to find the square 
// difference of two large 
// consecutive numbers 
  
  
class GFG{ 
   
// Function to find the square 
// difference of two large 
// consecutive numbers 
static long difference(long M, long N) 
{ 
    return M + N; 
} 
   
// Driver code 
public static void main(String[] args) 
{ 
    long M = 999999999; 
    long N = 1000000000; 
   
    System.out.print(difference(M, N) +"\n"); 
} 
} 
  
