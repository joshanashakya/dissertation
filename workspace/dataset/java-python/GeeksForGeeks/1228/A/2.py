

# Python3 program to find the volume 
# of the triangular prism 
  
# function to find the Volume  
# of triangular prism 
def findVolume(l, b, h) : 
  
    # formula to find Volume 
    return ((l * b * h) / 2) 
  
# Driver Code 
l = 18
b = 12
h = 9
      
# function calling 
print("Volume of triangular prism: ", 
                findVolume(l, b, h)) 

