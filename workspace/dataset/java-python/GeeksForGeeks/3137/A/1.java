

// Reversing a string using reverse() 
class GFG  
{ 
public static void main(String[] args) 
{ 
    StringBuilder str = new StringBuilder("geeksforgeeks"); 
      
    // Reverse str[beign..end] 
    str.reverse(); 
    System.out.println(str); 
} 
} 
  
