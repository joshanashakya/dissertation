

# Python3 program to illustrate  
# greatest integer Function  
import math 
  
# Function to calculate the  
# GIF value of a number  
def GIF(n): 
      
    # GIF is the floor of a number  
    return int(math.floor(n));  
  
# Driver code  
n = 2.3;  
  
print(GIF(n));  
      
