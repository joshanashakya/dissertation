

// Java implementation of the approach 
class GFG 
{ 
  
// Function to return the nth term 
// of the given series 
static int oddTriangularNumber(int N) 
{ 
    return (N * ((2 * N) - 1)); 
} 
  
// Driver code 
public static void main(String[] args)  
{ 
    int N = 3; 
    System.out.println(oddTriangularNumber(N)); 
} 
} 
  
