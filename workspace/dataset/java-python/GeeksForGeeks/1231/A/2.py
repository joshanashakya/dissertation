

# Python 3 program to find the  
# surface area Of Square pyramid 
  
# function to find the surface area 
def surfaceArea(b, s): 
  
    return 2 * b * s + pow(b, 2) 
  
# Driver Code 
if __name__ == "__main__": 
    b = 3
    s = 4
  
    # surface area of the square pyramid 
    print(surfaceArea(b, s)) 
  
