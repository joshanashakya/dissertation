

# Python3 code to illustrate 
# the use of cos function 
import math 
  
if __name__=='__main__': 
    PI = 3.14159265
  
    x = 60.0
    val = PI / 180.0
    ret = math.cos(x * val) 
    print("The cosine of is ", x, end=" ") 
    print(" degrees", ret) 
  
    x = 90.0
    val = PI / 180.0
    ret = math.cos(x * val) 
    print("The cosine of is ", x, end=" ") 
    print("degrees", ret) 
  
