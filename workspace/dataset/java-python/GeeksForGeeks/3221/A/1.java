

// Java program to perform sum of 
// cubes of first n odd natural numbers 
  
public class GFG  
{ 
    public static int cubesum(int n) 
    { 
                  
        return (n) * (n) * (2 * n * n - 1); 
    } 
      
  
    // Driver function 
    public static void main(String args[]) 
    { 
        int a = 4; 
        System.out.println(cubesum(a)); 
          
    } 
} 
  
