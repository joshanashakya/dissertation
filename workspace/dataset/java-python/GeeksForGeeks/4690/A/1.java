

// Java program to get the Average of  
// square of first n natural numbers 
import java.io.*; 
  
public class GFG{ 
  
// Function to get the average 
static float AvgofSquareN(int n) 
{ 
    return (float)((n + 1) * (2 *  
                    n + 1)) / 6; 
} 
  
// Driver Code 
static public void main (String[] args) 
{ 
    int n = 2; 
    System.out.println(AvgofSquareN(n)); 
  
} 
} 
  
