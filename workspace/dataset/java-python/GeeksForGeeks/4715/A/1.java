class GFG 
{ 
      
// Function to return the maximum LCM 
// among all the pairs(i, j) of 
// first n natural numbers 
static int maxLCM(int n) 
{ 
    return (n * (n - 1)); 
} 
  
// Driver code 
public static void main(String[] args) 
{ 
    int n = 3; 
  
    System.out.println(maxLCM(n)); 
} 
} 
  
