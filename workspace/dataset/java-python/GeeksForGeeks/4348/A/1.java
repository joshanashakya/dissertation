

// JAVA program to find the nth odd number 
  
class GFG 
{ 
    // Function to find the nth odd number 
    static int nthOdd(int n) 
    { 
        return (2 * n - 1); 
    } 
      
    // Driver code 
    public static void main(String [] args) 
    { 
        int n = 10; 
        System.out.println(nthOdd(n)); 
          
    } 
} 
  
