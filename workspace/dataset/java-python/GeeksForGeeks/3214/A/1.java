

// GCD of given range 
  
import java.io.*; 
  
class GFG { 
  
    static int rangeGCD(int n, int m) 
    { 
        return (n == m) ? n : 1; 
    } 
  
    public static void main(String[] args) 
    { 
        int n = 475; 
        int m = 475; 
          
        System.out.println(rangeGCD(n, m)); 
    } 
} 
  
