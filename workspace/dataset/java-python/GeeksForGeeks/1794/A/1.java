

// Java Program to find the Average of first 
// n natural numbers 
import java.io.*; 
  
class GFG { 
  
    // Return the average of first n  
    // natural numbers 
    static float avgOfFirstN(int n) 
    { 
        return (float)(1 + n) / 2; 
    } 
  
    // Driven Program 
    public static void main(String args[]) 
    { 
        int n = 20; 
        System.out.println(avgOfFirstN(n)); 
    } 
} 
  
