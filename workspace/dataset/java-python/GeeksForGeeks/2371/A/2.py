

# Python program to find percentage increase 
# in the volume of the cube 
# if a side of cube is increased 
# by a given percentage 
  
def newvol(x): 
  
    print("percentage increase"
            "in the volume of the cube is ", 
            ((x**(3)) / 10000 + 3 * x 
                + (3 * (x**(2))) / 100),"%"); 
  
x = 10; 
newvol(x); 
  
