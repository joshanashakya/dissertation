

// Java program to convert Fahrenheit 
// scale to Celsius scale 
class GFG { 
      
// function to convert 
// Fahrenheit to Celsius 
static float Conversion(float n) 
{  
    return (n - 32.0f) * 5.0f / 9.0f; 
} 
  
// Driver code 
public static void main(String[] args) { 
    float n = 40; 
    System.out.println(Conversion(n)); 
} 
} 
  
