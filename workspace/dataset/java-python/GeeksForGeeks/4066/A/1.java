

// Java program to find sum of 
// first n odd numbers 
import java.util.*; 
  
class Odd 
{    
    // Returns the sum of first 
    // n odd numbers 
    public static int oddSum(int n) 
    { 
        return (n * n); 
    } 
      
    // driver function 
    public static void main(String[] args) 
    { 
        int n = 20; 
        System.out.println(" Sum of first "+ n 
        +" Odd Numbers is: "+oddSum(n)); 
    } 
} 
  
