

// Java program to  
// find missing point 
// of a parallelogram 
import java.io.*; 
  
class GFG  
{ 
public static void main (String[] args)  
{ 
  
    int ax = 5, ay = 0; //coordinates of A 
    int bx = 1, by = 1; //coordinates of B 
    int cx = 2, cy = 5; //coordinates of C 
    System.out.println(ax + (cx - bx) + ", " + 
                       ay + (cy - by)); 
} 
} 
  
