for _ in range(int(input())):    n = int(input())
r = list(map(int, input().split()))
m = int(input())
b = list(map(int, input().split()))
p = list()
q = list()
for i in range(n):        l = sum(r[:(i + 1)])
p.append(l)
for j in range(m):        d = sum(b[:(j + 1)])
q.append(d)
a = max(p)
b = max(q)
print(max(0, a) + max(0, b))
